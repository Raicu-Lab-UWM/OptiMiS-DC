                     'Style','pushbutton',...
                     'cdata', Eapp_vs_XA_Icon_Image, 'TooltipString', 'Plot Eapp vs xA', ...
                     'Position',[90, 10, 50, 50], 'Units','pixels',...
                     'Visible','on',...
                     'FontSize', 10,...
                     'Callback', {@FRET_2D_Plot_EappXA_pButton_Callback, FRET_Tool_Kit_FigH});

[Hist2D_Icon_Image, ~] = imread('.\Icons\Surf_Plot_50x50_4.jpg');
FRET_2D_Calc_2DSpec_pButton = uicontrol('Parent', FRET_2D_Tab, 'Tag', 'FRET_2D_Calc_2DSpec_pButton', ...
                     'Style','pushbutton',...
                     'cdata', Hist2D_Icon_Image, 'TooltipString', '2D FRET Histogram', ...
                     'Position',[160, 10, 50, 50], 'Units','pixels',...
                     'Visible','on',...
                     'FontSize', 10,...
                     'Callback', {@FRET_2D_Calc_2DSpec_pButton_Callback});
end

