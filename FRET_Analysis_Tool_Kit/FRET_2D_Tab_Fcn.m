function FRET_2D_Tab_Fcn(FRET_Tool_Kit_FigH, FRET_2D_Tab)

[Calculate_Icon_Image, ~] = imread('.\Icons\Calculator_Button_50x50_3.png');
FRET_2D_Calc_Hist_pButton = uicontrol('Parent', FRET_2D_Tab, 'Tag', 'FRET_2D_Calc_Hist_pButton', ...
                     'Style','pushbutton',...
                     'cdata', Calculate_Icon_Image, 'TooltipString', 'Calculate FRET Data', ...
                     'Position',[20, 10, 50, 50], 'Units','pixels',...
                     'Visible','on',...
                     'FontSize', 10,...
                     'Callback', {@FRET_2D_Calc_Hist_pButton_Callback, FRET_Tool_Kit_FigH});

[Eapp_vs_XA_Icon_Image, ~] = imread('.\Icons\Eapp_vs_XA_50x50.png');
